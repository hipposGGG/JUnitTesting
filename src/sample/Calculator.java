package sample;

public class Calculator {

	/**
	 * x and yの乗算結果を返却
	 * @param x
	 * @param y
	 * @return x and yの乗算結果を返却
	 *
	 */
	public int maltiplication(int x,int y) {

		return x * y;
	}

	/**
	 * x and yの除算結果を返却
	 * @param x
	 * @param y
	 * @return x and yの除算結果を返却
	 *
	 */
	public float division(int x,int y) {

		return (float) x / (float) y;
	}



}
